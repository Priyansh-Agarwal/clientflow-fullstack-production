import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are an AI automation expert helping business owners configure ClientFlow AI, an enterprise AI automation platform. Your role is to:

1. Understand their business challenges and pain points
2. Recommend specific AI automation solutions
3. Guide them through configuring:
   - AI voice agents for customer calls
   - Appointment booking automation
   - Lead qualification systems
   - Multi-channel communication (SMS, email, WhatsApp)
   - CRM integrations
   - Review collection automation
   
4. Provide strategic advice on:
   - Optimizing call handling workflows
   - Improving conversion rates
   - Scaling operations efficiently
   - Measuring ROI and KPIs

Be conversational, professional, and solution-oriented. Ask clarifying questions to understand their specific needs. Provide actionable recommendations with clear implementation steps.

When users ask about features or configurations:
- Explain how to set them up in the platform
- Share best practices and industry benchmarks
- Suggest complementary features that could help
- Provide examples of successful implementations

Keep responses concise but comprehensive. Use bullet points for clarity when listing features or steps.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limits exceeded, please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Payment required, please add funds to your Lovable AI workspace." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
